#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int k;

	k = 0;
	i = 0;
	i = (int)ft_strlen(s1);
	while (s2[k])
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (s1);
}
